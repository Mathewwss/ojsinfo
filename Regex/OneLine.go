func OneLine (s *string) {
	// Regex patterns
	patterns := [][]string{
		[]string{"\t", " "},
		[]string{"\n", " "},
		[]string{"  *", " "},
		[]string{"^ ", ""},
		[]string{" $", ""},
	}

	// View list
	for a := 0; a < len(patterns); a++ {
		// Update string
		re := regexp.MustCompile(patterns[a][0])
		*s = re.ReplaceAllString(*s, patterns[a][1])

	}
}

