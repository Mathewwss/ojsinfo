func (c *DbCon) CheckCon () error {
	// Start variable
	var err error

	// Check struct
	if Db_conf.Opened == false && Db_conf.ConErr == nil {
		// Create new error
		err = errors.New("[ERROR] -> Database connection closed!")

	} else if Db_conf.Opened == false && Db_conf.ConErr != nil {
		// Update value
		err = Db_conf.ConErr

	}

	// Show error
	return err
}

